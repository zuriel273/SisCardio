    private void jButton_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_salvarActionPerformed
        String nome = jTextField_nome.getText();
        String dataNascimento = jFormattedTextField_dt_nascimento.getText();
        String cpf = jFormattedTextField_cpf.getText();
        String telefone = jFormattedTextField_telefone.getText();
        String sexo = jComboBox_sexo.getSelectedItem().toString();
        
        if(nome.trim().equals("") || dataNascimento.trim().equals("") || telefone.trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos são obrigatório. Preenchá-los.", "Alerta do Sistema",JOptionPane.WARNING_MESSAGE);
        }
        else{
            this.setAlwaysOnTop(false);
            Fachada f = new Fachada();
            Paciente p = new Paciente(nome, dataNascimento, cpf, sexo,telefone);
            try {
                f.cadastrarPaciente(p);
                jTextField_nome.setText(null);
                jFormattedTextField_cpf.setText(null);
                jFormattedTextField_dt_nascimento.setText(null);
                jFormattedTextField_telefone.setText(null);
                jButton_cancelar.setText("Voltar");
            } catch (PacienteErro ex) {
                Logger.getLogger(cadastroPaciente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton_salvarActionPerformed

