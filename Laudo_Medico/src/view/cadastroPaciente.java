    private void jFormattedTextField_cpfFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jFormattedTextField_cpfFocusLost
        String t = jFormattedTextField_cpf.getText().replace(".","").replace("-","").trim();
        this.cpfValido = (t.length() == 11);
        if(this.cpfValido && base.validaCPF(t)){
            jLabel_msg_cpf.setIcon(null);
            jLabel_msg_cpf.setText("");
        } 
        else {
            jLabel_msg_cpf.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/nook16x16.png")));
            jLabel_msg_cpf.setText("CPF inválido");
        }
        jButton_salvar.setEnabled(this.camposValidos());
    }//GEN-LAST:event_jFormattedTextField_cpfFocusLost

