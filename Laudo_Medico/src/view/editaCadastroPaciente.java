    private void jButton_atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_atualizarActionPerformed
        String nome = jTextField_nome.getText();
        String dataNascimento = jFormattedTextField_dt_nascimento.getText();
        String cpf = jFormattedTextField_cpf.getText();
        String telefone = jFormattedTextField_telefone.getText();
        String sexo = jComboBox_sexo.getSelectedItem().toString();
        
        this.setAlwaysOnTop(false);
        
        if(nome.trim().equals("") || dataNascimento.trim().equals("") || cpf.trim().equals("") || telefone.trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos são obrigatório. Preenchá-los.", "Alerta do Sistema",JOptionPane.WARNING_MESSAGE);
        }
        else{
            Fachada f = new Fachada();
            Paciente p = new Paciente(this.p.getId(),nome, dataNascimento, cpf, sexo,telefone);
            try {
                f.editarPaciente(p);
                jButton_cancelar.setText("Voltar");
              
            } catch (PacienteErro ex) {
                Logger.getLogger(editaCadastroPaciente.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        
    }//GEN-LAST:event_jButton_atualizarActionPerformed

    private void jFormattedTextField_cpfFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jFormattedTextField_cpfFocusLost
        String t = jFormattedTextField_cpf.getText().replace(".","").replace("-","").trim();
        this.cpfValido = (t.length() == 11);
        if(this.cpfValido && base.validaCPF(t)){
            jLabel_msg_cpf.setIcon(null);
            jLabel_msg_cpf.setText("");
        } 
        else {
            jLabel_msg_cpf.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/nook16x16.png")));
            jLabel_msg_cpf.setText("CPF inválido");
        }
        jButton_atualizar.setEnabled(this.camposValidos());
        
        this.setAlwaysOnTop(false);
        
    }//GEN-LAST:event_jFormattedTextField_cpfFocusLost

