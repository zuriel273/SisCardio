    private void jFormattedTextField_dt_nascimentoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jFormattedTextField_dt_nascimentoFocusLost
        Data dt = new Data();
        String nascimento = jFormattedTextField_dt_nascimento.getText();
        this.dataValida = dt.isDataValida(nascimento);
        if(this.dataValida){
            GregorianCalendar calendar = new GregorianCalendar();  
            int year = calendar.get(GregorianCalendar.YEAR);
            int month = calendar.get(GregorianCalendar.MONTH) + 1;
            int day = calendar.get(GregorianCalendar.DAY_OF_MONTH);
            
            String a = jFormattedTextField_dt_nascimento.getText().substring(6,nascimento.trim().length());
            String d = (String) jFormattedTextField_dt_nascimento.getText().subSequence(0,2);
            String m = (String) jFormattedTextField_dt_nascimento.getText().subSequence(3,5);
            
            int ano = Integer.parseInt(a);
            int mes = Integer.parseInt(m);
            int dia = Integer.parseInt(d);
            
            if(year > ano && ano > (year-200)){
                jLabel_msg_dt.setIcon(null);
                jLabel_msg_dt.setText("");
            }else if(year == ano){
               
                if(month < mes){
                    jLabel_msg_dt.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/nook16x16.png")));
                    jLabel_msg_dt.setText("Data inválido");
                    this.dataValida = false;
                }else if(month == mes && day < dia){
                    jLabel_msg_dt.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/nook16x16.png")));
                    jLabel_msg_dt.setText("Data inválido");
                    this.dataValida = false;
                }
               
            }else{
                jLabel_msg_dt.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/nook16x16.png")));
                jLabel_msg_dt.setText("Data inválido");
                this.dataValida = false;
            }
        } 
        else {
            jLabel_msg_dt.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/nook16x16.png")));
            jLabel_msg_dt.setText("Data inválida");
        }
        jButton_atualizar.setEnabled(this.camposValidos());
    }//GEN-LAST:event_jFormattedTextField_dt_nascimentoFocusLost
    
