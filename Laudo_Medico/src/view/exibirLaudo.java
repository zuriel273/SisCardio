    private void atualizarLista(int pacienteId) throws LaudoErro{
        
        this.setAlwaysOnTop(false);
        
        String [] colunas = new String []{"CÃ³digo","Data"};
        modelo = new DefaultTableModel(null, colunas){
            
        @Override
        public boolean isCellEditable(int row, int col){
                return false;
            }
        };
        
        Fachada f = new Fachada();
        List listar = f.listarLaudo(pacienteId);
        if(listar.isEmpty()){
            jButton_editar.setVisible(false);
            
        }
        
        Iterator it = listar.iterator();
        while(it.hasNext()){
            Laudo l = (Laudo)it.next();
            modelo.addRow(new Object[]{l.getId(),l.getData_criacao()});
        }
        
        jTable_laudo.setModel(modelo);
        jTable_laudo.setShowHorizontalLines(false);
        jTable_laudo.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        jTable_laudo.getColumnModel().getColumn(0).setPreferredWidth(80);   
        jTable_laudo.getColumnModel().getColumn(1).setPreferredWidth(120);  
    }
