    private void jButton_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_editarActionPerformed
        try {
            Fachada f = new Fachada();
            
            int numLinhaSelecionada = jTable_laudo.getSelectedRow();
            int id = Integer.parseInt(jTable_laudo.getValueAt(numLinhaSelecionada, 0).toString());

            Eco e = f.pesquisarLaudoPorId(id,"sub");
            if(e.getPaciente() != null || e.getId_paciente() != 0){
                this.setAlwaysOnTop(false);
                new editaCadastroSubjetivo(e, this).setVisible(true);
            }
            else {
               e = f.pesquisarLaudoEcoPorId(id);
              if(e.getPaciente() != null || e.getId_paciente() != 0){
                  this.setAlwaysOnTop(false);
                   new editarCadastroLaudoEco(e, this).setVisible(true);
              }
            }
           
         } catch (Exception ex) {
              this.setAlwaysOnTop(false);
              JOptionPane.showMessageDialog(this,"Selecione o Laudo.");
        }
            
        
    }//GEN-LAST:event_jButton_editarActionPerformed

    
