    private void jButton_incluir_laudoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_incluir_laudoActionPerformed
        try {
            
            int numLinhaSelecionada = jTable_pesquisado.getSelectedRow();
            int id = Integer.parseInt(jTable_pesquisado.getValueAt(numLinhaSelecionada, 0).toString());
            
            String nome = jTable_pesquisado.getValueAt(numLinhaSelecionada, 1).toString();
            String dataNascimento = jTable_pesquisado.getValueAt(numLinhaSelecionada, 2).toString();
            String cpf = jTable_pesquisado.getValueAt(numLinhaSelecionada, 3).toString();
            String sexo = jTable_pesquisado.getValueAt(numLinhaSelecionada, 4).toString();
            String telefone = jTable_pesquisado.getValueAt(numLinhaSelecionada, 5).toString();
            
            Paciente paciente = new Paciente(id, nome, dataNascimento, cpf, sexo, telefone);
            this.setAlwaysOnTop(false);
            new exibirLaudo(paciente).setVisible(true);
        
        } catch (Exception e){
            this.setAlwaysOnTop(false);
            JOptionPane.showMessageDialog(this,"Selecione o paciente.");
        }
    }//GEN-LAST:event_jButton_incluir_laudoActionPerformed

