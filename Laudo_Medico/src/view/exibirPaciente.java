    /**
     * 
     * @param pesquisa => Valor digitado
     * @throws PacienteErro 
     * 
     */
    public void atualizarLista(String pesquisa) throws PacienteErro{
        
        this.setAlwaysOnTop(false);
        
        String [] colunas = new String []{"Id do Paciente","Nome", "Data de Nascimento", "CPF", "Sexo","Telefone"};
        modelo = new DefaultTableModel(null, colunas){
            
        @Override
        public boolean isCellEditable(int row, int col){
                return false;
            }
        };
        Fachada f = new Fachada();
        List listar = f.listarPaciente(pesquisa);
        Iterator it = listar.iterator();
        while(it.hasNext()){
            Paciente p = (Paciente)it.next();
            modelo.addRow(new Object[]{p.getId(),p.getNome(),p.getDataNasc(),p.getCpf(),p.getSexo(),p.getTel()});
        }
        
        jTable_pesquisado.setModel(modelo);
        jTable_pesquisado.setShowHorizontalLines(false);
        jTable_pesquisado.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        /* Editado largura das Colunas
         * 0 - Id
         * 1 - Nome
         * 2 - Data de nascimento
         * 3 - Cpf
         * 4 - Sexo
         * 5 - Telefone
         */
        jTable_pesquisado.getColumnModel().getColumn(0).setPreferredWidth(80);   
        jTable_pesquisado.getColumnModel().getColumn(1).setPreferredWidth(120);  
        jTable_pesquisado.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTable_pesquisado.getColumnModel().getColumn(4).setPreferredWidth(40);
        jTable_pesquisado.getColumnModel().getColumn(5).setPreferredWidth(60);
        
    }
       
